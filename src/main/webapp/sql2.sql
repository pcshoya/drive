CREATE TABLE TBL_DRIVE (
    DRV_DATE CHAR(8),
    CAR_NO CHAR(10),
    DRV_START NUMBER,
    DRV_END NUMBER,
    DEPT_CODE CHAR(2),
    DRV_MONEY NUMBER,
    
    CONSTRAINT PK_DRIVE PRIMARY KEY (DRV_DATE, CAR_NO),
    CONSTRAINT FK_DRIVE_CAR FOREIGN KEY (CAR_NO)
        REFERENCES TBL_CAR(CAR_NO),
    CONSTRAINT FK_DRIVE_DEPT FOREIGN KEY (DEPT_CODE)
        REFERENCES TBL_DEPT(DEPT_CODE)
);

